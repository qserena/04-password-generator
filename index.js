const passwordOneElem = document.getElementById('password-one')
const passwordTwoElem = document.getElementById('password-two')

function generatePassword(passwordLength) {
	let password = ''
	for (let i = 0; i < passwordLength; i++) {
		const index = Math.floor(characters.length * Math.random())
		password += characters[index]
	}
	return password
}

function generatePasswords() {
	passwordOneElem.textContent = generatePassword(15)
	passwordTwoElem.textContent = generatePassword(15)
}
